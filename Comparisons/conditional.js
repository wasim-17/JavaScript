let messageLen = 10;

let maxMessageLen = 20;

console.log(
    `Trying to send a message of length:`, 
    messageLen,
    `and a max length of:`,
    maxMessageLen
);

if (messageLen > maxMessageLen) {
    console.log(`Message sent`);
} else {
    console.log(`Message not sent`);
}

