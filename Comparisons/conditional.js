/*
equality operators

=== equal to
!== not equal to
< less than
> greater than

<= less than or equal to
>= greater than or equal to

*/

console.log(5 < 6); // true
console.log(5 != 6); // true
console.log(5 <= 6); // true
console.log(5 !== 6); // true
console.log(5 >= 6); // false
console.log(5 == 6); // false
console.log(5 === 6); // false
console.log(5 > 6); // false
